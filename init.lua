-- Command to delete the current buffer
vim.keymap.set('n', '<leader>bd', function()
    local current_buf = vim.api.nvim_get_current_buf()
    local alternate_buf = vim.fn.bufnr('#')
    
    if alternate_buf ~= -1 and vim.api.nvim_buf_is_valid(alternate_buf) then
        vim.api.nvim_set_current_buf(alternate_buf)
    else
        vim.cmd('bnext')
    end
    
    vim.api.nvim_buf_delete(current_buf, { force = true })
end, { noremap = true, silent = true, desc = "Delete current buffer" })

